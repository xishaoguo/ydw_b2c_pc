    <h1 class="rules-title">
      药兜网平台食品经营者审查登记规范
    </h1><br>
    <div class="p-rules">
      <div class="ben-date a-weight">
        <p>第一章总则：</p>
      </div>
      <div class="list-title">
        一、目的和依据：
        <div class="d-weight">
          为规范药兜网平台食品生产经营者入驻审查登记标准，维护药兜网的正常经营秩序，保证消费者权益，根据《中华人民共和国食品安全法》等相关法律法规，以及相关协议和管理规范，特制订本规范。
        </div>
      </div>
      <div class="list-title">
        二、适用范围：
        <div class="d-weight">
          本规范适用于在药兜网平台开展食品零售业务的经营者。
        </div>
      </div>
      <div class="list-title">
        三、定义：
        <ul>
          <li>1、药兜网平台：指为商户提供食品展示及交易信息的第三方网络交易服务平台，以下简称“药兜网”。</li>
          <li>2、商户：指在药兜网平台开展食品经营活动的法人、其他组织。 </li>
          <li>3、用户：指在药兜网平台注册、接受平台规则，具有完全民事权利能力和行为能力的自然人。</li>
        </ul>
      </div>
      <div>
        <h1 style="font-size: 18px;">
          <p>
            第二章基本内容：
          </p>
        </h1>
      </div>
      <div class="list-title">
        四、入驻要求：
        <ul>
          <li>1、商户经营资质：(1)工商管理部门颁发的营业执照或其他替代营业执照的合法有效资质（下统称“营业执照”）。替代营业执照的其他有效资质包括事业单位法人证书、民办非企业单位登记证书、军队单位对外有偿服务许可证、社会团体法人登记证书等；(2)食药监督管理部门等行业管理部门颁布的行业许可资质文件。行业许可资质文件包括食品经营许可证、食品流通许可证、食品生产许可证、食品卫生许可证、全国工业产品生产许可证以及其他一些地方性政府承认的许可类证件，如食品经营实名备案证等。 </li>
          <li>2、法定代表人/商户负责人身份证件 商户提交的资质须满足上述要求且齐全。</li>
        </ul>
      </div>
      <div class="list-title">
        五、入驻审查标准 ：<br>
        1、资质信息：
        <ul class="wu-ul">
          <li style="height: 30px;line-height: 30px;border-top: 1px solid black;">
            <div style="line-height: 30px;">
              资质项目
            </div>
            <div>
              图片要求
            </div>
          </li>
          <li>
            <div>营业执照</div>
            <div>
              (1) 营业执照需提供原件；真实有效、提交审核时在有效期内；<br>
              (2) 营业执照需边框、国徽完整，拍摄清晰；<br>
              (3) 营业执照中注册号、名称、经营地址、经营范围、有效 期、发证日期、发照机关等重要信息不能被遮挡或涂改；<br>
              (4) 营业执照图片中不得出现除饿了么网及商户自己品牌以外 的其他水印；<br>
              (5) 营业执照图片不得造假（包括p图、假照等）;<br>
              (6) 营业执照的黑白复印件可以替代营业执照原件，复印件必 须加盖红色公章，公章文字需清晰且与营业执照名称一致。
            </div>
          </li>
          <li>
            <div>行业许可资质</div>
            <div>
              (1) 许可证需原件、真实有效、提交审核时证件在有效期内；<br>
              (2) 许可证需边框完整，拍摄清晰；<br>
              (3) 许可证中单位名称、证字号、地址、发证机关等重要信息 不能被遮挡或涂改；<br>
              (4) 许可证图片中不得出现除商户自己品牌以外的 其他水印；<br>
              (5) 许可证图片不得造假（包括 P 图、假证等）。
            </div>
          </li>
          <li>
            <div>法定代表人/商户负责人身份证件</div>
            <div>
              (1) 身份证件须为：有效大陆居民二代身份证、有效临时身份 证、有效驾驶证、有效护照、有效港澳居民来往内地通行 证、有效台湾居民来往大陆通行证、有效台胞证；<br>
              (2) 本人手持身份证件原件（带有照片面）拍照，并露出完整 五官；大陆居民二代身份证需加拍本人手持证件反面国徽面 一-张；<br>
              (3) 提供身份证件本人需年满 18 周岁；<br>
              (4) 身份证复印件需将正反面复印在同一张纸_上，证件本人 需按手印。
            </div>
          </li>
        </ul>
      </div>
      <div class="list-title">
        六、登记制度：
        <div class="d-weight">
          商户入驻药兜网平台均应按照平台登记流程和要求，在药兜网平台登记其真实有效的实际经营名称、营业资质、地址、法定代表人或者负责人及联系方式等信息。
        </div>
      </div>
      <div class="list-title">
        七、及时更新：
        <div class="d-weight">
          商户如提供的资质、信息或材料有变更的，应及时更新，以确保信息的准确性和有效性。如商户提供的资质、信息或材料错误、失效的，药兜网有权要求商户补充、上传资质、信息或材料。
        </div>
      </div>
      <div class="list-title">
        八、违规行为：
        <div class="d-weight">
          商户除遵守法律法规及本规范第五条入驻审查标准外，商户不得出现如下违规行为： 1、 商户提交的营业执照和行业资质主体名称不一致的； 2、商户营业执照和行业资质的经营范围与实际经营范围不一致或超范围经营的； 3、商户提供虚假资质、信息或材料的； 4、其他违反法律法规及双方协议、相关管理规范情形。
        </div>
      </div>
      <div>
        第三章 违规处理
      </div>
      <div class="list-title">
        九、违规处理：
        <ul>
          <li>1、药兜网平台有权对商户违规行为进行抽查监测。如发现商户存在违法违规情形的，药兜网平台有权根据违法违规行为严重程度对商户进行警告、整改、下线、关停等处理措施， 并对商户违法违规行为造成药兜网经济损失或其他纠纷、处罚的，药兜网有权追究商户相应责任并要求商户承担损失赔偿责任。 2、对于商户严重违法犯罪行为，药兜网有权交由行政管理机关或司法机关追究商户行政或刑事责任。</li>
        </ul>
      </div>
      <div class="list-title">
        十、申诉流程：
        <ul>
          <li>
            1、针对药兜网对商户的违规处理措施，如商户认为不合理的，则有权依据本规范在收到违规处理之日起2日内向药兜网提起违规申诉，如商户2日内未进行违规申诉的，视为商户认可违规处罚。2、为了保证申诉的有效性，提高申诉成功率，商户申诉时需按照药兜网平台的要求提供完整、真实、有效的证明材料，包括但不限于：聊天记录、截图、链接、照片、录音、快递单凭证、报案回执等。如商户提供虚假证明材料的或经药兜网核验证明材料与事实不符，即视为申诉不成功。 3、药兜网在收到商户提起申诉后，将及时审核商户提交的申诉材料并反馈相应处理结果。若违规申诉成功，
            则对应的违规处罚不成立、处罚措施不执行；若申诉不成功，则对应的违规处罚成立、处罚措施将直接执行。
          </li>
        </ul>
      </div>
      <div>
        第四章其他 ：
      </div>
      <div class="list-title">
