      <div class="list-title">
        七、不合格医疗器械管理制度：
        <ul>
          <li>
            1、平台质量管理部负责做好不合格医疗器械的管理工作。如因主观原因导致不合格医疗器械进入流通渠道，将视其情节轻重，给予有关人员相应的处罚。
          </li>
          <li>2、不合格医疗器械是指质量不符合法定的质量标准或相关法律法规及规章的要求，包括内在质量和外在质量不合格的医疗器械。</li>
          <li>
            3、不合格医疗器械的确认：<br>
            3.1、质量验收人员在验收的过程当中发现的外观质量、包装质量不符合要求的或通过质量复检确认为不合格的；<br>
            3.2、医疗器械监督管理部门的质量公报品种、通知禁售的品种，并经公司质量管理部核对确认的；<br>
            3.3、在保管养护过程中发现过期、失效、淘汰及其他有质量问题的医疗器械；
          </li>
          <li>
            4、不合格的处理<br>
            4.1、器械入库验收过程中发现不合格产品，应上报质管理部确认，存放不合格品库，挂红牌标志后上报业务部处理。<br>
            4.2、养护员在器械养护检查过程中发现不合格品，应填写“不合格器械的登记表”报质量管理部进行确认。<br>
            4.3、在器械养护、出库、复核或上级药监部门抽查过程中发现确认的不合格器械，都要立即停止配送、发运和销售，同时按出库复核记录追回己发出的不合格器械。<br>
            4.4、不合格器械应由专人保管并建立台帐，按规定进行报废审批和销毁。<br>
            4.5、对质量不合格器械，应查明原因，分清责任，及时纠正并制定出预防措施。<br>
            4.6、认真及时地做好不合格器械上报、确认处理、报损和销毁记录，记录应妥善保存五年。<br>
            4.7、平台要及时对不合格医疗器械进行下架处理。
          </li>
          <li>
            5、不合格医疗器械的报告：<br>
            5.1、在入库验收过程中发现不合格品，应存放于不合格品区，报质量管理部，同时填写“不合格器械通知处理单”，并及时通知供货方，明确退货或报废销毁等处理办法。<br>
            5.3、药监部门检查中发现的或公布的不合格医疗器械，要立即进行追回，集中置于不合格品区，按照监管部门的意见处置。
          </li>
          <li>
            6、发生质量问题的相关记录，销毁不合格品的相关记录及明细表，应予以保存5年备查。
          </li>
        </ul>
