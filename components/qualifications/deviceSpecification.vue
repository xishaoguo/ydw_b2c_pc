        十二、违规行为：
        <div>
          商户除遵守法律法规及本规范第五条入驻审查标准外，商户不得出现如下违规行为： 1、 商户提交的营业执照和行业资质主体名称不一致的； 2、商户营业执照和行业资质的经营范围与实际经营范围不一致或超范围经营的； 3、商户提供虚假资质、信息或材料的； 4、其他违反法律法规及双方协议、相关管理规范情形。
        </div>
