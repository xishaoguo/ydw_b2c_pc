        六、供应商医疗器械验收标准：
        <ul>
          <li>1、企业收货人员在接收医疗器械时，应当核实运输方式及产品是否符合要求，并对照相关采购记录和随货同行单与到货的医疗器械进行核对。交货和收货双方应当对交运情况当场签字确认。对不符合要求的产品应当立即报告质量负责人并拒收。</li>
