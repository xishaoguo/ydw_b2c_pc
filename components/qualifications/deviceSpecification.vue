            3.3、验收记录上应当标记验收人员姓名和验收日期。验收不合格的还应当注明不合格事项及处置措施。<br>
            3.4、对需要冷藏、冷冻的医疗器械进行验收时，应当对其运输方式及运输过程的温度记录、运输时间、到货温度等质量控制状况进行重点检查并记录，不符合温度要求的予以拒收。
          </li>
