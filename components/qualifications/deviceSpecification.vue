            2、随货同行单应当包括供货者、生产企业及生产企业许可证号（或者备案凭证编号）、医疗器械的名称、规格（型号）、注册证号或者备案凭证编号、生产批号或者序列号、数量、储运条件、收货单位、收货地址、发货日期等内容，并加盖供货者出库印章。
            收货人员对符合收货要求的医疗器械，应当按品种特性要求放于相应待验区域，或者设置状态标示，并通知验收人员进行验收。需要冷藏、冷冻的医疗器械应当在冷库内待验区待验。
          </li>
