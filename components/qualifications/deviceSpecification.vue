        九、医疗器械销售和售后管理服务制度：
        <ul>
          <li>1、经营者销售医疗器械，必须提供加盖本企业公章的授权书。授权书应当载明授权销售的品种、地域、期限，注明销售人员的身份证号码和联系方式。</li>
          <li>2、所有销售业务，流向都必须是合法的购货者，销售前应当对购货者的资质材料、经营范围进行核实，经质量管理审核合格，并建立购货者档案后，方可开展业务，确保医疗器械销售流向真实、合法。</li>
          <li>
            3、销售的产品需建立“销售记录（清单）”<br>
            （1）医疗器械的名称、规格（型号）、注册证号或者备案凭证编号、数量、单价、金额；<br>
            （2）医疗器械的生产批号或者序列号、有效期、销售日期；<br>
            （3）生产企业和生产企业许可证号（或者备案凭证编号）。<br>
            （4）购货者的名称、经营许可证号（或者备案凭证编号）、经营地址、联系方式。
          </li>
          <li>4、销售产品应开具合法票据，做到票、帐、货相符，并按规定建立购销记录。一次性使用无菌医疗器械的销售记录必须真实完整，其内容应有：销售日期、销售对象、销售数量、产品名称、生产单位、型号规格、生产批号、灭菌批号、产品有效期、经办人、负责人签名等。</li>
          <li>5、凡经质量管理部检查确认或按上级药监部门通知的不合格医疗器械，一律不得开票销售，已销售的应及时通知收回，并按质量不合格产品管理制度和程序执行。</li>
          <li>6、销售产品时应正确介绍产品，不得虚假夸大和误导用户。</li>
        </ul>
        售后服务：
        <ul>
          <li>1、医疗器械产品有其独立的特殊性，品种多、种类多，不同品种间技术要求等级差别大，有的技术性高，使用过程中可能出现的变化因素多，情况较复杂，根据产品特性的较高，必须搞好相关的售后服务。</li>
          <li>2、应根据实际，售后服务的内容包括投诉渠道及方式、处理措施、反馈和事后跟踪等。</li>
          <li>3、对于一些特殊产品，在必要的时候，可采取跟踪售后服务。</li>
          <li>
            4、售后服务的主要任务：<br>
            1) 向客户解答其咨询的有关的产品质量情况，使用情况。<br>
            2) 接受客户的意见、反馈的信息，协助解决具体问题，维修和保养，并进行跟踪。<br>
            3) 向客户解释医疗器械的性能和注意事项。<br>
            4) 向客户征求对产品的改进意见，咨询市场信息。<br>
            5）市场中可能遇到的其它偶发问题等。
          </li>
        </ul>
