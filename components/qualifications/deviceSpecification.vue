        <ul>
          <li>1、平台接到临床使用机构或用户的可疑不良事件信息后，应当填写“可疑医疗器械不良事件报告表”向本辖区省医疗器械不良事件监测技术机构报告。其中，导致死亡的事件于发现或者知悉之日起 24 小时内上报；导致严重伤害、可能导致严重伤害或非死亡的事件于发现或者知悉之日起 3个工作日内上报。</li>
          <li>2、当发现突发、群发的医疗器械不良事件，应当立即向本辖区省食品医疗器械监督管理部门、卫生主管部门和医疗器械不良事件监测技术机构报告，并在 24 小时内填写并报送“可疑医疗器械不良事件报告表”。如有必要，可以越级报告，但是应当及时告知被越过的所在地省、自治区、直辖市食品医疗器械监督管理部门、卫生主管部门和医疗器械不良事件监测技术机构。</li>
          <li>3、质量管理部负责可疑医疗器械不良事件报告的具体工作。</li>
          <li>4、销售部在接到联络员报出的可疑不良医疗器械检测报告事件，第一时间要进行产品来源追溯，通过采购员向上一级经销商或生产商进行追溯，确保一级一级来源可追。</li>
        </ul>
